export type InsightItem = { what: string; why: string; effect: string };

export function buildMissingConfidenceItems(input: StartupAnalysisInput, result: StartupAnalysisResult): InsightItem[] {
  const out: InsightItem[] = [];
  if (input.retentionD30 <= 0) out.push({ what: "Нет данных по удержанию D30", why: "Пока здесь слишком мало подтверждения, чтобы считать вывод устойчивым.", effect: "Добавление этих данных снизит неопределённость в интерпретации." });
  if ((input.topCustomerSharePct ?? 0) <= 0 && (input.top3CustomersSharePct ?? 0) <= 0) out.push({ what: "Недостаточно данных о концентрации выручки", why: "Это один из ключевых недостающих сигналов для более уверенного чтения кейса.", effect: "Без этих данных итог остаётся осторожнее, чем мог бы быть." });
  if ((result.moatEvidenceScore ?? 0) <= 0 && (input.moatStrength ?? 0) > 0) out.push({ what: "Moat пока больше заявлен, чем подтверждён", why: "Сейчас это один из главных пробелов в доказательной базе проекта.", effect: "Добавление этих данных снизит неопределённость в интерпретации." });
  if ((input.stage === "idea" || input.mode === "idea") && (result.stageEvidenceScore ?? 0) < 45) out.push({ what: "Недостаточно подтверждения спроса для ранней стадии", why: "Это один из ключевых недостающих сигналов для более уверенного чтения кейса.", effect: "Без этих данных итог остаётся осторожнее, чем мог бы быть." });
  if ((input.nrrPct ?? 0) <= 0 && (input.grrPct ?? 0) <= 0 && (input.stage === "series_b" || input.stage === "growth" || input.stage === "exit")) {
    out.push({ what: "Нет данных по удержанию выручки (NRR/GRR)", why: "Это один из ключевых недостающих сигналов для более уверенного чтения кейса.", effect: "Добавление этих данных снизит неопределённость в интерпретации." });
  }
  return out.slice(0, 5);
}

export function buildThesisWeakeningItems(input: StartupAnalysisInput, result: StartupAnalysisResult): string[] {
  const out: string[] = [];
  if (input.retentionD30 > 0) out.push("Если этот фактор ухудшится, текущий тезис станет заметно слабее: дальнейшее ухудшение удержания.");
  out.push("Это один из самых чувствительных сигналов для ухудшения итоговой оценки: рост CAC без улучшения активации.");
  if ((result.runwayMonths ?? 0) > 0) out.push("Здесь находится один из основных downside-триггеров: сокращение запаса денег.");
  if ((result.concentrationRiskScore ?? 0) > 0 || (input.topCustomerSharePct ?? 0) > 0) out.push("Даже при сильных сторонах ухудшение этого показателя быстро портит общую картину: рост концентрации выручки.");
  out.push("Это тот случай, когда один негативный сдвиг может потянуть вниз несколько секций сразу: падение повторяемости выручки.");
  return out.slice(0, 5);
}
